    private boolean exportToCSV(String pathToExportTo) {

    try {

        TableModel model = table.getModel();
        FileWriter csv = new FileWriter(new File(pathToExportTo));

        csv.write("\n");
        csv.write("\n");
        
        csv.write("Macnine N 0"+model.getValueAt(0, 0).toString());
        csv.write(" ;");
        csv.write(" ;");
        csv.write(" ;");
        csv.write(" ;");
        csv.write(" ;");
        csv.write("Date :"+model.getValueAt(0, 6).toString());
        csv.write("\n");
        for (int i = 0; i < model.getColumnCount(); i++) {
            csv.write(model.getColumnName(i) + ";");
        }

        csv.write("\n");

        for (int i = 0; i < model.getRowCount(); i++) {
            for (int j = 0; j < model.getColumnCount(); j++) {
                csv.write(model.getValueAt(i, j).toString() + ";");
            }
            csv.write("\n");
        }

        csv.close();
        return true;
    } catch (IOException e) {
        e.printStackTrace();
    }
    return false;
}
